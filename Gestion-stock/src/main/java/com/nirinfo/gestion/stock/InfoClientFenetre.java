    public void update(Client client){
        try {
            PreparedStatement pst = con.prepareStatement("UPDATE client set nom=?, prenom=?, adresse=?, contact=? WHERE idClient=?");
            pst.setString(1, client.getNom());
            pst.setString(2, client.getPrenom());
            pst.setString(3, client.getAdresse());
            pst.setInt(4, client.getContact());
            pst.setInt(5, client.getId());
            pst.executeUpdate();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erreur lors de la mise Ã  jour : " + ex.getMessage());
        }
    }
    
    private void updateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateMouseClicked
        // TODO add your handling code here:
        if(client != null){
            client.setNom(nom.getText());
            client.setPrenom(prenom.getText());
            client.setAdresse(adresse.getText());
            client.setContact(Integer.valueOf(contact.getText()));
            update(client);
            fCLient.rafraichir(); 
            this.dispose();
        }
    }//GEN-LAST:event_updateMouseClicked

    public void delete(int id){
        try {
            PreparedStatement pst = con.prepareStatement("DELETE FROM client WHERE idClient=?");
            pst.setInt(1, id);

            pst.executeUpdate();
        } catch (Exception e) {
        }
    }
    
    private void deleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteMouseClicked
        // TODO add your handling code here:
        delete(client.getId());
        this.dispose();
        fCLient.rafraichir();
    }//GEN-LAST:event_deleteMouseClicked

