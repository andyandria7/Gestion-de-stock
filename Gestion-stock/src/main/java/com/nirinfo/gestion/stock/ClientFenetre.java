    public void insert(Client client) {
        try {
            PreparedStatement pst = con.prepareStatement("INSERT INTO client (nom, prenom, adresse,contact) VALUE (?,?,?,?)");
            pst.setString(1, client.getNom());
            pst.setString(2, client.getPrenom());
            pst.setString(3, client.getAdresse());
            pst.setInt(4, client.getContact());

            pst.executeUpdate();
        } catch (SQLException ex) {
            Logger.getLogger(FournisseurFenetre.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void ajoutMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ajoutMouseClicked
        // TODO add your handling code here:
        String name = nom.getText();
        String username = prenom.getText();
        String adrs = adresse.getText();
        String contacts = contact.getText();

        try {
            PreparedStatement pst = con.prepareStatement("SELECT * FROM client WHERE contact=?");
            pst.setString(1, contacts);
            ResultSet rs = pst.executeQuery();

            if (name.isEmpty() || username.isEmpty() || adrs.isEmpty() || contacts.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Veuillez remplir tous les champs !!");

            } else if (rs.next()) {
                JOptionPane.showMessageDialog(this, "Le contact existe déja");
                contact.setText("");
            } else {
                Client client = new Client();
                client.setNom(name);
                client.setPrenom(username);
                client.setAdresse(adrs);
                client.setContact(Integer.valueOf(contacts));

                insert(client);
                affiche();
                nom.setText("");
                prenom.setText("");
                adresse.setText("");
                contact.setText("");
            }
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(this, "Une erreur c'est produit" + e.getMessage());
        }
    }//GEN-LAST:event_ajoutMouseClicked

    public Client getId(int id) {
        try {
            PreparedStatement pst = con.prepareCall("SELECT * FROM client WHERE idClient=?");
            pst.setInt(1, id);
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                return new Client(
                        rs.getInt("idClient"),
                        rs.getString("nom"),
                        rs.getString("prenom"),
                        rs.getString("adresse"),
                        rs.getInt("contact")
                );
            }
        } catch (SQLException ex) {
            Logger.getLogger(FournisseurFenetre.class.getName()).log(Level.SEVERE, null, ex);
        }
        return null;
    }

    private void infoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_infoMouseClicked
        // TODO add your handling code here:
        if (selectedId == -1) {
            JOptionPane.showMessageDialog(this, "Veuillez sélectionner une ligne dans la table.");
            return;
        }
        Client client = this.getId(selectedId);

        if (client != null) {
            InfoClientFenetre iclient = new InfoClientFenetre(client, this);
            iclient.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Aucune information trouvée pour ce client.");
        }
    }//GEN-LAST:event_infoMouseClicked

