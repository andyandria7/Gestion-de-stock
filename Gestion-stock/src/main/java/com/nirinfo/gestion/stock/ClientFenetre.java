    String[][] tabList() {
        String[][] liste = new String[this.getNumberRow()][5];
        try {
            Statement state = con.createStatement();
            ResultSet rs = state.executeQuery("SELECT * FROM client");
            int i = 0;
            while (rs.next()) {
                liste[i][0] = rs.getString("idClient");
                liste[i][1] = rs.getString("nom");
                liste[i][2] = rs.getString("prenom");
                liste[i][3] = rs.getString("adresse");
                liste[i][4] = rs.getString("contact");
                i++;
            }
        } catch (SQLException ex) {
            Logger.getLogger(FournisseurFenetre.class.getName()).log(Level.SEVERE, null, ex);
        }

        return liste;
    }

    int getNumberRow() {
        int n = 0;
        try {
            Statement state = con.createStatement();
            ResultSet rs = state.executeQuery("SELECT * FROM client");
            while (rs.next()) {
                n++;
            }
        } catch (SQLException ex) {
            Logger.getLogger(FournisseurFenetre.class.getName()).log(Level.SEVERE, null, ex);
        }
        return n;
    }

