    String[][] tabList() {
        String[][] liste = new String[this.getNumberRow()][4];
        try {
            Statement state = con.createStatement();
            ResultSet rs = state.executeQuery("SELECT * FROM fournisseur");
            int i = 0;
            while (rs.next()) {
                liste[i][0] = rs.getString("idFournisseur");
                liste[i][1] = rs.getString("nomF");
                liste[i][2] = rs.getString("adresseF");
                liste[i][3] = rs.getString("contactF");
                i++;
            }
        } catch (SQLException ex) {
            Logger.getLogger(FournisseurFenetre.class.getName()).log(Level.SEVERE, null, ex);
        }

        return liste;
    }

    int getNumberRow() {
        int n = 0;
        try {
            Statement state = con.createStatement();
            ResultSet rs = state.executeQuery("SELECT * FROM fournisseur");
            while (rs.next()) {
                n++;
            }
        } catch (SQLException ex) {
            Logger.getLogger(FournisseurFenetre.class.getName()).log(Level.SEVERE, null, ex);
        }
        return n;
    }

