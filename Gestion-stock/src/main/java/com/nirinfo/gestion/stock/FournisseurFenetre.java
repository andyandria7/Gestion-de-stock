    public Fournisseur getId(int id) {
        try {
            PreparedStatement pst = con.prepareCall("SELECT * FROM fournisseur WHERE idFournisseur=?");
            pst.setInt(1, id);
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                return new Fournisseur(
                        rs.getInt("idFournisseur"),
                        rs.getString("nomF"),
                        rs.getString("adresseF"),
                        rs.getInt("contactF")
                );
            }
        } catch (SQLException ex) {
            Logger.getLogger(FournisseurFenetre.class.getName()).log(Level.SEVERE, null, ex);
        }
        return null;
    }

    private void infoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_infoMouseClicked
        // TODO add your handling code here:
        if (selectedId == -1) {
            JOptionPane.showMessageDialog(this, "Veuillez sélectionner une ligne dans la table.");
            return;
        }
        Fournisseur fo = this.getId(selectedId);

        if (fo != null) {
            InfoFournisseurFenetre info = new InfoFournisseurFenetre(fo, this);
            info.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Aucune information trouvée pour ce fournisseur.");
        }

    }//GEN-LAST:event_infoMouseClicked

