    public void insert(Fournisseur fo) {
        try {
            PreparedStatement pst = con.prepareStatement("INSERT INTO fournisseur (nomF, adresseF, contactF) VALUE (?,?,?)");
            pst.setString(1, fo.getNomF());
            pst.setString(2, fo.getAdresseF());
            pst.setInt(3, fo.getContactF());
            pst.executeUpdate();
        } catch (SQLException ex) {
            Logger.getLogger(FournisseurFenetre.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void ValiderMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ValiderMouseClicked
        try {
            // TODO add your handling code here:
            String nomF = nom.getText();
            String adresseF = adresse.getText();
            String contactF = contact.getText();

            PreparedStatement pst = con.prepareStatement("SELECT * FROM fournisseur WHERE contactF=?");
            pst.setString(1, contactF);
            ResultSet rs = pst.executeQuery();

            if (nomF.isEmpty() || adresseF.isEmpty() || contactF.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Veuillez remplir tous les champs !!");

            } else if (rs.next()) {
                JOptionPane.showMessageDialog(this, "Le contact existe déja");
                contact.setText("");
            } else {
                Fournisseur f = new Fournisseur();
                f.setNom(nomF);
                f.setAdresse(adresseF);
                f.setContact(Integer.valueOf(contactF));

                insert(f);
                affiche();
                nom.setText("");
                adresse.setText("");
                contact.setText("");
            }

        } catch (SQLException ex) {
            Logger.getLogger(FournisseurFenetre.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_ValiderMouseClicked

