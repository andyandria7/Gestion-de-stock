    public void update(Fournisseur fo) {
        try {
            PreparedStatement pst = con.prepareStatement("UPDATE fournisseur set nomF=?, adresseF=?, contactF=? WHERE idFournisseur=?");
            pst.setString(1, fo.getNomF());
            pst.setString(2, fo.getAdresseF());
            pst.setInt(3, fo.getContactF());
            pst.setInt(4, fo.getIdFournisseur());
            pst.executeUpdate();
        } catch (SQLException ex) {
            Logger.getLogger(InfoFournisseurFenetre.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Erreur lors de la mise Ã  jour : " + ex.getMessage());
        }
    }
    
    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseClicked
        // TODO add your handling code here:
        if(fo != null){
            fo.setNom(nom.getText());
            fo.setAdresse(adresse.getText());
            fo.setContact(Integer.valueOf(contact.getText()));
            update(fo);
            fe.affiche();
            this.dispose();
        }
    }//GEN-LAST:event_btnUpdateMouseClicked

        public void delete(int id){
            try {
                PreparedStatement pst = con.prepareStatement("DELETE FROM fournisseur WHERE idFournisseur=?");
                pst.setInt(1, id);

                pst.executeUpdate();
            } catch (Exception e) {
            }
        }
    
